#include "ft_printf.h"

void	ft_putuns(unsigned int nb)
{
	int	aux;

	aux = 0;
	
	aux = nb % 10;
	nb = nb / 10;
	if (nb > 0)
	{
		ft_putuns(nb);
	}
	aux = aux + 48;
	write(1, &aux, 1);
}
